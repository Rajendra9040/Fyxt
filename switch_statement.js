let a = parseInt(prompt('Enter an integer between 1 to 7 : '))
switch(a){
    case 1 :
        document.write('monday')
        break;
    case 2 :
        document.write('Tuesday')
        break;
    
    case 3 :
        document.write('wednessday')
        break;
    
    case 4 :
        document.write('Thursday')
        break;
    
    case 5 :
        document.write('friday')
        break;
    
    case 6 :
        document.write('saturay')
        break;
    
    case 7 :
        document.write('sunday')
        break;
    
    default :
    document.write('wrong input')
}

